// Helper: search products with fuzzy matching
export function searchProducts(query: string): IndianProduct[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];

  // Score each product for relevance
  const scored = INDIAN_PRODUCTS.map(product => {
    let score = 0;
    const name = product.name.toLowerCase();
    const brand = product.brand.toLowerCase();
    const category = product.category.toLowerCase();
    const description = product.description.toLowerCase();

    // Exact brand match gets highest score
    if (brand === q) score += 100;
    // Brand starts with query
    else if (brand.startsWith(q)) score += 80;
    // Brand contains query
    else if (brand.includes(q)) score += 60;

    // Name contains query
    if (name.includes(q)) score += 50;

    // Category match
    if (category.includes(q)) score += 40;

    // Description match
    if (description.includes(q)) score += 20;

    // Check individual words in query
    const words = q.split(/\s+/);
    for (const word of words) {
      if (word.length < 2) continue;
      if (brand.includes(word)) score += 30;
      if (name.includes(word)) score += 25;
      if (category.includes(word)) score += 15;
      if (description.includes(word)) score += 10;
      // Check ingredients
      if (product.ingredients.some(ing => ing.toLowerCase().includes(word))) {
        score += 5;
      }
    }

    return { product, score };
  });

  // Return products with score > 0, sorted by relevance, max 16
  return scored
    .filter(s => s.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, 16)
    .map(s => s.product);
}
